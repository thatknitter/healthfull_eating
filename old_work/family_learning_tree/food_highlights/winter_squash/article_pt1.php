			<p class="article">
				There are so many different kinds of squash that it's sometimes hard to believe they're all from the same family. All squash, whether they 
				are classified as summer squash or winter squash, are really edible gourds. Winter squash is the name given to the plants in this family that 
				develop tough skins which allow them to be stored and kept over the winter. Although there were only a few varieties for many years, dozens 
				of new varieties are now available, including some stunningly rich-tasting ones.
			</br>
			</br>
				Nearly all winter squash have a sweet, yellow or orange flesh which is rich in beta-carotene &#8230 a Vitamin A precursor that helps protect 
				against cancer, heart problems and respiratory disease. People eating plenty of orange or orange-yellow vegetables in the squash family run 
				a lower risk of contracting several different kinds of cancer, most notably lung cancer.
			</br>
			</br>
				Winter squash differ in shape, size, color and density and yet, many people assume they all taste the same. This couldn't be farther from the 
				truth! They all have different tastes as well as texture, and each variety has its cache of ardent fans &#8230 while you may be partial to certain 
				squash varieties, I can assure you that your friends and even family will feel equally strongly about others. The best way to decide is to 
				try as many as you can, and then decide. Which is what we'll aim to do over the coming months.
			</br>
			</br>
				But there is more to these gourds than just the flesh. A dark green, aromatic oil is pressed from the seeds of pumpkins which is excellent 
				for dressing all kinds of salads. And whatever you do, don't throw out the seeds &#8211 they are not only nutritious, but utterly delicious when 
				toasted. Even the pumpkin and squash blossoms are considered a delicacy by some when dipped in batter and fried. Young pumpkin leaves are a 
				superb source of Vitamin C and Beta Carotene, and in Italy, Asia and Africa the leaves are valued as a cooked vegetable.
			</br>
			</br>
				Let's get on our tour bus and explore the different winter squash varieties on the market today.
